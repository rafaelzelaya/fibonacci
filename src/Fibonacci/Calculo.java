    private void BtnFibonacciActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnFibonacciActionPerformed
        pila = new java.util.Stack<>();
        txtSecuencia.setText("");
        int posicion = Integer.parseInt(txtPosicion.getText());
        int valor = Fibonacci(posicion);
        JOptionPane.showMessageDialog(this, "El Fibonacci de "
                +posicion+" es "+valor);
        for(int i : pila){
            txtSecuencia.setText(txtSecuencia.getText()+" "+i);
        }
    }//GEN-LAST:event_BtnFibonacciActionPerformed
    private java.util.Stack<Integer> pila = new java.util.Stack();
    private int Fibonacci(int posicion){
        posicion = java.lang.Math.abs(posicion);
        if(posicion < 2) return posicion;
        else{
            int respuesta = Fibonacci(posicion - 1) + Fibonacci(posicion - 2);
            pila.push(respuesta);
            return respuesta;
        }
    }
